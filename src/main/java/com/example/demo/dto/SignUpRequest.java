package com.example.demo.dto;

public class SignUpRequest {

    private String name;
    private String nickName;
    private int age;
    private String gender;

    public String getName() {
        return name;
    }

    public String getNickName() {
        return nickName;
    }

    public int getAge() {
        return age;
    }

    public String getGender() {
        return gender;
    }
}
